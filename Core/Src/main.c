void resetAdc() {
	HAL_GPIO_WritePin(ADC_PORT, ADC_CS_PIN, GPIO_PIN_SET);
	HAL_GPIO_WritePin(ADC_PORT, ADC_SCLK_PIN, GPIO_PIN_SET);
	HAL_GPIO_WritePin(ADC_PORT, ADC_RESET_PIN, GPIO_PIN_SET);
	for (int i = 0; i < 64; i++) {
	}
	HAL_GPIO_WritePin(ADC_PORT, ADC_RESET_PIN, GPIO_PIN_RESET);
}

// Asks the ADC to start converting its inputs to digital values.
void requestAdcStartConverting() {
	HAL_GPIO_WritePin(ADC_PORT, ADC_CONVST_PIN, GPIO_PIN_RESET);
	for (int i = 0; i < 64; i++) {
	}
	HAL_GPIO_WritePin(ADC_PORT, ADC_CONVST_PIN, GPIO_PIN_SET);
}

void readDataFromAdc() {
	HAL_GPIO_WritePin(ADC_PORT, ADC_CS_PIN, GPIO_PIN_RESET);
	for (int channel = 0; channel < ADC_NUM_CHANNELS; channel++) {
		adcData[1 + channel] = 0;
		for (int bit = 0; bit < ADC_BITS_PER_CHANNEL; bit++) {
			HAL_GPIO_WritePin(ADC_PORT, ADC_SCLK_PIN, GPIO_PIN_RESET);
			adcData[1 + channel] <<= 1;
			adcData[1 + channel] |=
					HAL_GPIO_ReadPin(ADC_PORT, ADC_DOUTA_PIN) == GPIO_PIN_SET ?
							1 : 0;
			HAL_GPIO_WritePin(ADC_PORT, ADC_SCLK_PIN, GPIO_PIN_SET);
			for (int i = 0; i < 2; i++) {
			}
		}
	}
	HAL_GPIO_WritePin(ADC_PORT, ADC_CS_PIN, GPIO_PIN_SET);
}

void readAdcTask(void *argument) {
	resetAdc();
	for (;;) {
		requestAdcStartConverting();
		// Wait for the interrupt to say it's our time to resume again.
		ulTaskNotifyTake(pdTRUE, 1000);
		readDataFromAdc();
		appendDataFrame(&adcData[0], 70);
	}
}

